public class destinationAddress {
    int buildingNumber;
    String street;
    String city;
    String state;
    String country;
    boolean buisnessAddress;
    String specialInstructions;
    public destinationAddress (int buildingNumber, String street, String city, String state, String country, boolean buisnessAddress, String specialInstructions) {
        this.buildingNumber = buildingNumber;
        this.street = street;
        this.city = city;
        this.state = state;
        this.country = country;
        this.buisnessAddress = buisnessAddress;
        this.specialInstructions = specialInstructions;
    }

}
